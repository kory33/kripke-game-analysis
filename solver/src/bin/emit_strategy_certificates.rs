extern crate kripke_game_solver;

use kripke_game_solver::finite_kripke_frame::FiniteKripkeFrame;
use kripke_game_solver::offical_game_solver::{
    StrategyAgainstFixedWorldCount, answer_distribution_for_query, come_up_with_strategy,
};
use std::io::{self, Write};

fn frame_ids_to_lean_multiset(frames: &[&'static FiniteKripkeFrame<4>], indent: usize) -> String {
    let indent_str = "  ".repeat(indent);
    if frames.is_empty() {
        "frameIdsAsMultiset #[]".to_string()
    } else {
        let mut result = String::from("frameIdsAsMultiset #[");
        for (i, frame) in frames.iter().enumerate() {
            if i > 0 {
                result.push_str(", ");
            }
            if i % 16 == 0 {
                result.push('\n');
                result.push_str(&indent_str);
                result.push_str("  ");
            }
            result.push_str(&frame.to_u16_id().to_string());
        }
        result.push('\n');
        result.push_str(&indent_str);
        result.push(']');
        result
    }
}

fn certificate_to_lean(
    strategy: &StrategyAgainstFixedWorldCount,
    possible_frames: Vec<&'static FiniteKripkeFrame<4>>,
    indent: usize,
) -> String {
    let indent_str = "  ".repeat(indent);
    match strategy {
        StrategyAgainstFixedWorldCount::ProceedWithExhaustiveSearch => {
            format!(
                "KripkeGameStrategy.StrategyCertificate.exhaustive\n{}  ({})",
                indent_str,
                frame_ids_to_lean_multiset(&possible_frames, indent + 1)
            )
        }
        StrategyAgainstFixedWorldCount::AskQueryAndThen { query, cont } => {
            let buckets = answer_distribution_for_query(&possible_frames, query);
            let children = (0..=4u8)
                .map(|answer| {
                    let child_strategy = cont
                        .get(&answer)
                        .map(|child| child.as_ref())
                        .unwrap_or(&StrategyAgainstFixedWorldCount::ProceedWithExhaustiveSearch);
                    let child_frames = buckets.get(&answer).cloned().unwrap_or_default();
                    certificate_to_lean(child_strategy, child_frames, indent + 2)
                })
                .collect::<Vec<_>>();

            let children_str = children
                .iter()
                .map(|child| format!("\n{}    {}", indent_str, child))
                .collect::<Vec<_>>()
                .join(",");

            format!(
                "KripkeGameStrategy.StrategyCertificate.ask\n{}  ({})\n{}  #[{}{}  ]",
                indent_str,
                frame_ids_to_lean_multiset(&possible_frames, indent + 1),
                indent_str,
                children_str,
                format!("\n{}", indent_str)
            )
        }
    }
}

fn main() -> io::Result<()> {
    let strategy = come_up_with_strategy();
    let grouped = FiniteKripkeFrame::<4>::canonical_frames_grouped_by_accessibility_count();

    let stdout = io::stdout();
    let mut output = stdout.lock();

    writeln!(
        output,
        "-- DO NOT EDIT MANUALLY: Autogenerated by the Kripke Game solver in `/solver`"
    )?;
    writeln!(output)?;
    writeln!(
        output,
        "import KripkeGameAnalysis.Game.Strategy.CertificateValidation"
    )?;
    writeln!(
        output,
        "import KripkeGameAnalysis.Game.Strategy.gen.Strategy"
    )?;
    writeln!(
        output,
        "import KripkeGameAnalysis.Game.Strategy.gen.PrecomputedFrameSets"
    )?;
    writeln!(output)?;
    writeln!(output, "open KripkeGameAnalysis.Precomputed")?;
    writeln!(output)?;
    writeln!(output, "namespace KripkeGameAnalysis.Generated")?;
    writeln!(output)?;
    writeln!(
        output,
        "private def frameIdsAsMultiset (frameIds : Array Nat) : Multiset (Fin 65536) :="
    )?;
    writeln!(
        output,
        "  frameIds.toList.map (fun frameId => (⟨frameId, by native_decide⟩ : Fin 65536))"
    )?;
    writeln!(output)?;

    for relation_count in 0u8..=16 {
        let strategy_for_count = strategy.0.get(&relation_count).unwrap();
        let frames = grouped.get(&relation_count).cloned().unwrap_or_default();
        let certificate_name = format!("certificate_for_relation_count_{}", relation_count);

        writeln!(
            output,
            "def {} : KripkeGameStrategy.StrategyCertificate :=",
            certificate_name
        )?;
        writeln!(
            output,
            "  {}",
            certificate_to_lean(strategy_for_count, frames.clone(), 1)
        )?;
        writeln!(output)?;

        writeln!(output, "theorem {}_rootFrames_eq :", certificate_name)?;
        writeln!(
            output,
            "    {}.rootFrames = frameIdsSet_{}.val := by",
            certificate_name, relation_count
        )?;
        writeln!(output, "  native_decide")?;
        writeln!(output)?;

        writeln!(output, "theorem {}_valid :", certificate_name)?;
        writeln!(
            output,
            "    KripkeGameStrategy.StrategyCertificate.proves strategy_for_relation_count_{} 10 {} = true := by",
            relation_count, certificate_name
        )?;
        writeln!(output, "  native_decide")?;
        writeln!(output)?;

        writeln!(
            output,
            "theorem strategy_for_relation_count_{}_is_winning_on_frames :",
            relation_count
        )?;
        writeln!(
            output,
            "    KripkeGameStrategy.is_winning_strategy_on_frames strategy_for_relation_count_{} 10 frameIdsSet_{}.val = true := by",
            relation_count, relation_count
        )?;
        writeln!(
            output,
            "  have h := KripkeGameStrategy.StrategyCertificate.proves_sound strategy_for_relation_count_{} 10 {} {}_valid",
            relation_count, certificate_name, certificate_name
        )?;
        writeln!(
            output,
            "  simpa [{}_rootFrames_eq] using h",
            certificate_name
        )?;
        writeln!(output)?;
    }

    writeln!(output, "end KripkeGameAnalysis.Generated")?;
    Ok(())
}
